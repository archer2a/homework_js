function capitalizeWords(str) {
    const words = str.split(' '); // 1. –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ –º–∞—Å—Å–∏–≤ —Å–ª–æ–≤
    const capitalizedWords = [];

    for (let i = 0; i < words.length; i++) {
        const word = words[i];
        if (word.length > 0) {
            // 2. –ë–µ—Ä–µ–º –ø–µ—Ä–≤—É—é –±—É–∫–≤—É, –¥–µ–ª–∞–µ–º –µ–µ –∑–∞–≥–ª–∞–≤–Ω–æ–π
            const firstLetter = word.charAt(0).toUpperCase();
            // 3. –ë–µ—Ä–µ–º –æ—Å—Ç–∞–ª—å–Ω—É—é —á–∞—Å—Ç—å —Å–ª–æ–≤–∞
            const restOfWord = word.slice(1).toLowerCase(); // `toLowerCase()` –º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å, —á—Ç–æ–±—ã –æ—Å—Ç–∞–ª—å–Ω–∞—è —á–∞—Å—Ç—å –±—ã–ª–∞ —Å—Ç—Ä–æ—á–Ω–æ–π
            // 4. –û–±—ä–µ–¥–∏–Ω—è–µ–º –∏ –¥–æ–±–∞–≤–ª—è–µ–º –≤ –Ω–æ–≤—ã–π –º–∞—Å—Å–∏–≤
            capitalizedWords.push(firstLetter + restOfWord);
        } else {
            capitalizedWords.push(''); // –î–æ–±–∞–≤–ª—è–µ–º –ø—É—Å—Ç—É—é —Å—Ç—Ä–æ–∫—É, –µ—Å–ª–∏ —Å–ª–æ–≤–æ –±—ã–ª–æ –ø—É—Å—Ç—ã–º
        }
    }

    return capitalizedWords.join(' '); // 5. –°–æ–±–∏—Ä–∞–µ–º —Å–ª–æ–≤–∞ –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
}


